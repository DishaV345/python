#meaning of charac
#  "r" -> reading file(by default)
#  "w" -> open for writing,truncating the file first(new data)(old data gets deleted)
#  "x" -> create and open new file for writing
#  "a" -> appending at the end of a file, for writing
#  "t" -> opens txt file(by default)
#  "b" -> opens binary files
#   "+" -> opens disk file for updating(reading and writing)
# "r+" -> read,overwrite,no truncate,pointer at start
#   "w+" -> write,no truncate,pointer at start
# "a+"   -> append at end,pointer at end,no truncate